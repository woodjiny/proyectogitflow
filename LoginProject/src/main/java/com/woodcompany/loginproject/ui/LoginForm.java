    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if(Validacion.isValid(new String[]{UserField.getText(), String.valueOf(PassField.getPassword())})){
                SeeLabel.setText("Sesi칩n iniciada");
                SeeLabel.setForeground(Color.green);
                
                MainPanel mainPanel = new MainPanel();
                this.setVisible(false);
                mainPanel.setVisible(true);
                
            } else{
                SeeLabel.setText("Usuario/Contrase침a incorrecta");
                SeeLabel.setForeground(Color.red);
            }
        } catch (SQLException ex) {
            Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

